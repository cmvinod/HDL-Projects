
module mod5_modern(CNT,clk,rst);
input clk,rst;
output reg [2:0] CNT;

wire[2:0] din;

assign din = (CNT==4)? 0 : CNT+1;

always @(posedge clk, posedge rst)
begin
	if(rst) CNT <= 0;
	else    CNT <= din;
end


endmodule










